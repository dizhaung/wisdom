package com.javahelps.wisdom.query.tree;

public class AnnotationElement {

    private String key;
    private Object value;

    public void setKey(String key) {
        this.key = key;
    }

    public String getKey() {
        return key;
    }

    public Object getValue() {
        return value;
    }

    public void setValue(Object value) {
        this.value = value;
    }
}
